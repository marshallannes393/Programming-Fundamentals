#include <iostream>
using namespace std;

int main() {
    int n;
    cout << "Number: "; 
cin >> n;

    if (n == 25) cout << "twenty five" << endl; 
    else if (n == 42) cout << "forty two" << endl; 
    else if (n == 6) cout << "six" << endl; 
    else if (n == 75) cout << "Seventy Five" << endl; 
    return 0;
}