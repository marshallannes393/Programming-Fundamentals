#include <iostream>
using namespace std;

int main() {
    cout << "Multiplication Table of 24:" << endl;
    for(int i = 1; i <= 10; i++) {
        cout << "24 * " << i << " = " << 24 * i << endl;
    }

    cout << endl << "Multiplication Table of 50:" << endl;
    for(int i = 1; i <= 10; i++) {
        cout << "50 * " << i << " = " << 50 * i << endl;
    }

    cout << endl << "Multiplication Table of 29:" << endl;
    for(int i = 1; i <= 10; i++) {
        cout << "29 * " << i << " = " << 29 * i << endl;
    }
    return 0;
}